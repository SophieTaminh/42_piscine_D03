char		*ft_strrev(char *str)
{
	int		n;
	int		i;
	char	c;
	int		l;

	n = 0;
	while (str[n] != '\0')
	{
		n++;
	}
	i = 0;
	l = n / 2;
	while (i < l)
	{
		c = str[i];
		str[i] = str[n - 1 - i];
		str[n - i - 1] = c;
		i++;
	}
	return (str);
}
